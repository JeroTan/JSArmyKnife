interface SVG{
  viewBox: string;
  width: string;
  height: string;
  xmlns: string;
}
export interface VECTORS{
  element: string;
  d?: string;
  fillRule?: string;
  clipRule?: string;
  [key: (string|number)]:any
}
export interface ICON{
  svg: SVG;
  vectors: VECTORS[];
}

//A class that you will be use to add those Icons
export class MakeIcon{
  private svg: SVG = {
      viewBox:"0 0 24 24",
      width:"100%",
      height:"100%",
      xmlns:"http://www.w3.org/2000/svg",
  };
  private vectors: VECTORS[] = [];

  constructor(){
      this.reset();
  }
  reset(){
      //This.svg is the parent of all vectors
      this.svg = {
          viewBox:"0 0 24 24",
          width:"100%",
          height:"100%",
          xmlns:"http://www.w3.org/2000/svg",
      }
      //This.vectors is the child of svg that will be use to show a graphics
      this.vectors = [];
      return this;
  }
  addFill(d:string){
      this.vectors.push({
          element:"path",
          d:d,
          fill:"currentColor",
      })
      return this;
  }
  addStroke(d:string, fillRule:string="evenodd", clipRule:string="evenodd"){
      this.vectors.push({
          element: "path",
          fillRule: fillRule,
          clipRule: clipRule,
          stroke:"currentColor",
          d:d,
      })
      return this;
  }
  addPath(element:object){
      this.vectors.push({
          element: "path",
          ...element
      })
      return this;
  }
  addViewBox(viewBox:string){
      this.svg.viewBox = viewBox;
      return this;
  }
  get(){
      const iconData: ICON = {
          svg: this.svg,
          vectors: this.vectors,
      }
      return iconData;
  }
}

//iconNew is use to make an icons
export function iconNew(){
  return new MakeIcon();
}


